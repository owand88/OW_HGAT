, sum(ck.SI_BI_GMV2) as sold_items
, 0 as total_listings
, 0 as currently_live_listings

FROM FOCUSED_VERT_TXN ck
INNER JOIN DW_CAL_DT cal
	on ck.GMV_DT = cal.CAL_DT
	and cal.RETAIL_YEAR in (2022,2023)
INNER JOIN sellers s
	on s.id = ck.seller_id
LEFT JOIN DW_USERS u
	on ck.seller_id = u.user_id

Where 1=1
and ck.FOCUSED_VERTICAL_LVL1 in ('Handbags >=$500','Jewelry >=$300','Watches >=$500')
and ck.RPRTD_WACKO_YN = 'N'

Group by 1,2,3,4,5,6


